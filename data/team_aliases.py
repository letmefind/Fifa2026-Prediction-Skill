def normalize_team_name(name: str) -> str:
    if not name or not str(name).strip():
        return str(name)
    key = str(name).lower().strip()
    return TEAM_ALIASES.get(key, str(name).strip())


def teams_match(team_a: str, team_b: str) -> bool:
    return normalize_team_name(team_a) == normalize_team_name(team_b)


def fixture_pair_key(team_a: str, team_b: str) -> frozenset[str]:
    return frozenset({normalize_team_name(team_a), normalize_team_name(team_b)})
